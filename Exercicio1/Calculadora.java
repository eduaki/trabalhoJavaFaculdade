package Exercicio1;

public class Calculadora {

static float soma(float n1, float n2){

    float resultado = n1 + n2;
    return resultado;
}
static float sub(float n1, float n2){

    float resultado = n1 - n2;
    return resultado;
}
static float multi(float n1, float n2){

    float resultado = n1 * n2;
    return resultado;
}
static float div(float n1, float n2){

    float resultado = n1 / n2;
    return resultado;
}




}
